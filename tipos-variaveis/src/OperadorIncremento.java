public class OperadorIncremento {
    public static void main(String[]args){

        int numero =5;

        numero +=2; //SOMA

        System.out.println(numero); 
        
        numero ++; //
        System.out.println(numero);


        //NEGAÇÃO 
        boolean verdadeira = true;

        verdadeira = !verdadeira;
        System.out.println(verdadeira); //negação 
    }
}
